#import "CBColour.h"

@implementation CBColour

+ (UIColor*)blue
{
	return [UIColor colorWithRed:0 green:.478431 blue:1 alpha:1];
}

+ (UIColor*)green
{
	return [UIColor colorWithRed:.16 green:.772 blue:.098 alpha:1];
}

+ (UIColor*)grey
{
	return [UIColor colorWithWhite:.392275 alpha:.35];
}

@end
